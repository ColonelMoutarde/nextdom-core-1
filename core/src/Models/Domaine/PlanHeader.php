namespace NextDom\src\Models\Domaine;


class PlanHeader
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var string
     */
    private $name;

    /**
     * @var string
     */
    private $image;

    /**
     * @var string
     */
    private $configuration;

    /**
     * @return int
     */
    public function getId(): int
    {
        return $this->id;
    }

    /**
     * @param int $id
     * @return PlanHeader
     */
    public function setId(int $id): PlanHeader
    {
        $this->id = $id;
        return $this;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @param string $name
     * @return PlanHeader
     */
    public function setName($name): PlanHeader
    {
        $this->name = $name;
        return $this;
    }

    /**
     * @return string
     */
    public function getImage(): string
    {
        return $this->image;
    }

    /**
     * @param string $image
     * @return PlanHeader
     */
    public function setImage($image): PlanHeader
    {
        $this->image = $image;
        return $this;
    }

    /**
     * @return string
     */
    public function getConfiguration(): string
    {
        return $this->configuration;
    }

    /**
     * @param string $configuration
     * @return PlanHeader
     */
    public function setConfiguration($configuration): PlanHeader
    {
        $this->configuration = $configuration;
        return $this;
    }

}